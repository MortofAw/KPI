#include <stdio.h>
#include <math.h>

int main() {
	float x, h, a = -2, b = 2, n = 7,f1,f2,f3;

	h = (b - a) / n;

	printf("------------------------------------------------------------------------------------\n");
	printf("|                             Function of one variable                             |\n");
	printf("|----------------------------------------------------------------------------------|\n");
	printf("|       Arg X       |  f1 = 1-e^(|x|+2)  |   f2 = 2^(x-4)-3   |  f3 = ln|24x + 2|  |\n");

	for (int i = 0; i <= n; i++) { 
		printf("|----------------------------------------------------------------------------------|\n");
		x = a + i * h;
		f1 = 1 - exp(fabs(x) + 2);
		f2 = exp((x - 4) * log(2)) - 3;
		if (fabs(24 * x + 2)!=0){
			f3 = log(fabs(24 * x + 2));
		}
		else {
			printf("!f3 doesn't exist!");
		}
		printf("|      %-13.3f|       %-13.3f|       %-13.3f|      %-13.3f |\n", x, f1, f2, f3);		
	}
	printf("------------------------------------------------------------------------------------\n");
	printf("|                                                       By: Fesenko Denis - KV-34  |\n");
	printf("------------------------------------------------------------------------------------\n");

	return 0;
}